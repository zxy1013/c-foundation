#include<iostream>
using namespace std;

//结构体定义 一些类型的集合
struct student
{
	//成员列表 属性
	string name;  //姓名
	int age;      //年龄
	int score;    //分数
}stu3; //结构体变量创建方式3 


int main26() {
	//结构体创建变量时 struct 关键字可以省略

	//结构体变量创建方式1
	struct student stu1; 
	stu1.name = "张三";
	stu1.age = 18;
	stu1.score = 100;
	cout << "姓名：" << stu1.name << " 年龄：" << stu1.age << " 分数：" << stu1.score << endl;


	//结构体变量创建方式2
	struct student stu2 = { "李四",19,60 };
	cout << "姓名：" << stu2.name << " 年龄：" << stu2.age << " 分数：" << stu2.score << endl;


	//结构体变量创建方式3赋值
	stu3.name = "王五";
	stu3.age = 18;
	stu3.score = 80;
	cout << "姓名：" << stu3.name << " 年龄：" << stu3.age << " 分数：" << stu3.score << endl;


	//结构体数组
	struct student arr[3] =
	{
		{"张三",18,80 },
		{"李四",19,60 },
		{"王五",20,70 }
	};
	//改变赋值
	arr[1].name ="zz ";
	for (int i = 0; i < 3; i++)
	{
		cout << "姓名：" << arr[i].name << " 年龄：" << arr[i].age << " 分数：" << arr[i].score << endl;
	}


	//结构体指针
	//创建结构体变量
	struct student stu = { "张三",18,100,};
	//创建指针指向结构体变量
	struct student* p = &stu;
	//通过->操作符可以访问成员
	p->score = 80; 
	cout << "姓名：" << p->name << " 年龄：" << p->age << " 分数：" << p->score << endl;

	system("pause");
	return 0;
}
